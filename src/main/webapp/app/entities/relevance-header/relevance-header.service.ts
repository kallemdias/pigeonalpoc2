import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { IRelevanceHeader } from '@/shared/model/relevance-header.model';

const baseApiUrl = 'api/relevance-headers';

export default class RelevanceHeaderService {
  public find(id: number): Promise<IRelevanceHeader> {
    return new Promise<IRelevanceHeader>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IRelevanceHeader): Promise<IRelevanceHeader> {
    return new Promise<IRelevanceHeader>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IRelevanceHeader): Promise<IRelevanceHeader> {
    return new Promise<IRelevanceHeader>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IRelevanceHeader): Promise<IRelevanceHeader> {
    return new Promise<IRelevanceHeader>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
