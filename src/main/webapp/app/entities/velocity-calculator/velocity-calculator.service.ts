import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { IVelocityCalculator } from '@/shared/model/velocity-calculator.model';

const baseApiUrl = 'api/velocity-calculators';

export default class VelocityCalculatorService {
  public find(id: number): Promise<IVelocityCalculator> {
    return new Promise<IVelocityCalculator>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IVelocityCalculator): Promise<IVelocityCalculator> {
    return new Promise<IVelocityCalculator>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IVelocityCalculator): Promise<IVelocityCalculator> {
    return new Promise<IVelocityCalculator>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IVelocityCalculator): Promise<IVelocityCalculator> {
    return new Promise<IVelocityCalculator>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
